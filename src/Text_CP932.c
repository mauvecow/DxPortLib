unsigned int PL_Text_ReadSJISChar(const char **pStr) {
    unsigned char *str = (unsigned char *)*pStr;
    unsigned int ch = *str;
    if (ch == 0) {
        return 0;
    }
    
    str += 1;
    
    if (ch >= 0x80) {
        if ((ch >= 0x81 && ch <= 0x9f) || (ch >= 0xe0 && ch <= 0xfc)) {
            if (*str != 0) {
                ch = (ch << 8) | *str;
                str += 1;
            }
        }
        ch = s_TableSJISToUnicode_Main[s_TableSJISToUnicode_Lookup[ch >> 6] + (ch & 63)];
    }
    
    *pStr = (char *)str;
    
    return ch;
}

int PL_Text_WriteSJISChar(char *pStr, unsigned int ch, int maxLen) {
    unsigned char *str = (unsigned char *)pStr;
    if (ch < 0 || ch > 65535) {
        return 0;
    }
    
    ch = s_TableUnicodeToSJIS_Main[s_TableUnicodeToSJIS_Lookup[ch >> 6] + (ch & 63)];

    if (ch < 0x80) {
        if (maxLen >= 1) {
            str[0] = (unsigned char)ch;
            return 1;
        }
    } else if (maxLen >= 2) {
        str[0] = (unsigned char)(ch >> 8);
        str[1] = (unsigned char)(ch);
        return 2;
    }
    
    return 0;
}

